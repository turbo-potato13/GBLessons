package com.vtb.kortunov.lesson7;

public class MyEntry {

    private final Integer key;
    private final Integer value;

    public MyEntry(Integer key, Integer value) {
        this.key = key;
        this.value = value;
    }

    public Integer getKey() {
        return key;
    }

    public Integer getValue() {
        return value;
    }
}
